			<p>
				<strong>Kulturkort for ungdom</strong>
				<br />
				Kortet gir unge mellom 16 og 20 år i Oslo tilgang på billetter med 20-50% rabatt på kunst og kulturopplevelser ved 26 institusjoner. Formålet er at unge skal benytte seg av kulturtilbudene oftere, og at nye publikummere skal rekrutteres. Kulturkortet er gratis, og gyldig sammen med aldersbevis fra du har fylt 16 til den dagen du fyller 20.
				<br />
			</p>
			<div id="merinfoKulturkort">
				<div class="close_button"> X </div>
				<img src="./BilderWebb/kulturaktiviteter/kulturkortet.jpg" Alt="" width="250">
				<p>

					All ungdom i målgruppen vil i løpet av en treårig prøveperiode få tildelt kulturkort.

					For alle
					Ordningen er ikke forbeholdt elever i videregående skole, men skal også gi tilbud til de rundt 7000 unge som ikke er i skolesystemet. Oslo har totalt 23 000 unge i målgruppen.

					Hvordan få tak i kort?
					Elever i videregående skole får kortet utdelt på skolen. Andre kan hente sitt kort ved samtlige av Deichmanske biblioteks filialer, på Popsentret på Schaus plass eller ved UngInfo i Møllergata 3.

					Source:<a href="kulturetaten.oslo.kommune.no" target="blank">kulturetaten.oslo.kommune.no</a></p>
