	<td>	<p><strong>STEEN OG STRØM</strong><br>
			Åpningstider: man-fre 10-19 lør 10-18<br>

			Steen og Strøm åpnet i 1874 Kristianias første stormagasin i et stort nybygg i fire etasjer på Kongens gate 23, tegnet av arkitekt Paul Due. 
			Men senere, i 1929, brant dette ned. 
			Etter brannen ble dagens stormagasin i fem etasjer og tre tilbaketrukne etasjer oppført på samme tomt etter tegninger av arkitekt Ole Sverre. <br></p>
			
			<div id="merinfoSteenStrom">
				<div class="close_button"> X </div>

				<img src="BilderWebb/steenstrom.jpg" Alt="Bilde fra Steen og Strøm" width="150">
			<p>Det sto ferdig i 1930 og har et åpent ovalt midtrom gjennom etasjene under et stort glasstak. 
				Magasinet fikk byens første rulletrapper. 
				Siden det har nye og moderne shoppingsentere blitt bygget rundt om i de forskjellige landene, og kjeden regnes som en av de største i Norden.<br>
			Fra:<a href="http://no.wikipedia.org/wiki/Steen_%26_Str%C3%B8m" target="blank"> www.wikipedia.no</a><br>
			Bilde:<a href="http://farm6.staticflickr.com/5186/5637779959_8d620ffd17_o.jpg" target="blank">www.flicr.com</a><br>
			<a href="http://www.steenogstrommagasin.no/butikker" target="blank">Mer info?</a>
			</p>
