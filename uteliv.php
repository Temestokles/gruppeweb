			<td>	<h4>JAEGER</h4>
			<p>Med internasjonale bookings i helgene og faste konsepter i ukedagene har Jaeger på kort tid blitt
			en av de mest populære og innflytelsesrike utestedene i Oslo.
			Lokalisert på grensen 9 midt i hjertet av hovedstaden er klubben blitt ett knutepunkt
			mellom øst og vest og trekker ett publikum som strekker seg langt utover de faste klubbgåerene i byen.<br/>
			<span id="lesmerJæger">Les mer..</span></p>
			<p id="merinfoJæger">Gratis før midnatt, 100kr etter. Diskon åpner 23:00. ID 23 fra 23:00.<br/>
			På dagtid kan du drikke kaffe, ta en pils eller spise lunsj i caféen som holder åpent frem til 18.30 hver dag<br/>
			og på kveldstid har vi ett spekket klubbprogram fra mandag til og med søndag.<br/>
			Source: <a href="http://www.jægeroslo.no">www.jægeroslo.no</a></p></td>

