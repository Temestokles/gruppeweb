					<p>

						Kunst i Oslo forvalter også Oslo kommunes kunstordning.
						Denne ordningen gjør at det avsettes 0,5 prosent av av kommunens totalte investeringsbudsjett til kunstprosjekter.
						Regelverket gjelder alle Oslo kommunes virksomheter og anvendes ved for eksempel kommunale nybygg, rehabilitering, idrettsanlegg, trafikkanlegg, park- og byrom.

